package com.company;

public  class Calc {
    public double plus (double frst, double scnd){
        return frst+scnd;
    }
    public double minus (double frst, double scnd){
        return frst-scnd;
    }
    public double multiply (double frst, double scnd){
        return frst*scnd;
    }
    public double div (double frst, double scnd){
        return frst/scnd;
    }
}